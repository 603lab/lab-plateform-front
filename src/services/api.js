export async function queryProjectList(params) {
  const result = getFakeList({ ...params });
  const req = {};
  if (result.length) {
    req.statusCode = 200;
    req.msg = '';
    req.data = result;
  } else {
    req.statusCode = 0;
    req.msg = '网络超时';
    req.data = [];
  }
  return {
    ...req,
  };
}

